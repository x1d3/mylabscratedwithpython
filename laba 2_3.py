# Константы
R = 8.314  # универсальная газовая постоянная в Дж/(моль·К)
P_Pa = 4.0e5  # давление в Паскалях
T_Celsius = 100  # температура в градусах Цельсия

# Пересчет температуры в Кельвины
T_K = T_Celsius + 273.15

# Пересчет давления из Н/м² в Паскали
P_Pa = P_Pa

# Вычисление количества вещества
n = (P_Pa * 1) / (R * T_K)

# Молярная масса воздуха (г/моль)
molar_mass_air = 29

# Вычисление плотности воздуха в г/л
rho_air_g_per_l = n * molar_mass_air

print(f"Плотность воздуха при {T_Celsius}°C и {P_Pa} Н/м²: {rho_air_g_per_l:.2f} г/л")